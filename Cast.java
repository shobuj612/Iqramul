public class Cast {
    public static void main(String[] args) {
        

        // byte convert to short

          byte aa=120;
          short _aa_=aa;

          System.out.println(_aa_);


          // byte convert to char

          char Nt=780;
          System.out.println(Nt);

          byte dd=66;
            
          char aaa= (char)dd;
          System.out.println(aaa);

          // byte convert to char

          byte amar = 120;

          char amar1 = (char)amar;

          System.out.println(amar1);
          
        // byte convert to integer

        byte samar = 120;

        int samar1 = samar;

        System.out.println(samar1);


        // byte convert into long


        byte akash = 44;
          

        long akasha = akash ;
          
        System.out.println(akasha);


        // byte convert into a float

        byte india = 55;

           
        float _india_ = india ;
        

        System.out.println(_india_);



        // byte convert into a  double

         byte  ___a__=45;

         double _2s__= ___a__ ;

         System.out.println(_2s__);



          

        // short convert into a char


        short mukesh = 123;

        char mukesh_=(char)mukesh ;

        char _____123__=(char) mukesh;


        System.out.println(_____123__);

        System.out.println(mukesh_);
               
       // short convert into integer

       short __jamIa= 456 ;

       int __456__=__jamIa ;

       
       System.out.println(__jamIa);
       System.out.println(__456__);


       //short convert into float


       short __india45___=7852 ;


       float ___451__f_ = __india45___;

       System.out.println(__india45___);
       System.out.println(___451__f_);


       // short convert into double

       short __fun__ = 4556;

       double fun= __fun__;


       System.out.println(__fun__);
       System.out.println(fun);

       
       //char convert into integer
       
       char chr__=150;

       int __char_=chr__;

       int _love_ = (int)chr__;

       System.out.println(__char_);

       System.out.println(_love_);


       char _1_=160;

       char _2_ = 170;

       long _3_ = _1_;
       long _4_ = _2_;

       System.out.println(_4_);
       System.out.println(_3_);



       // char  convert into float  

       char _5__=1801;

       char __6__= 25000;

       float __7__ = _5__;
       float __8__ = __6__;

       System.out.println(__8__);
       System.out.println(__7__);

     
       // char convert into double


       char _9_ = 789;

       char _10_ = 1000;

       double _11_ = _9_;

       double _12_=_10_;

       
       System.out.println(_11_);
       System.out.println(_12_);



    }
}
